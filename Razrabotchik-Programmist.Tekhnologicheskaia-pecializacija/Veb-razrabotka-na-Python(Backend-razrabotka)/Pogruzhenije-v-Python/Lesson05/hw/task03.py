"""
Напишите генераторную функцию fibonacci(n), которая принимает на вход
одно целое число n и возвращает последовательность первых n чисел
Фибоначчи. Числа Фибоначчи — это последовательность, в которой каждое
число является суммой двух предыдущих, начиная с 0 и 1.
"""

def fibonacci(n):
    a, b = 0, 1  # Начальные значения Фибоначчи
    for _ in range(n):
        yield a  # Возвращаем текущее число Фибоначчи
        a, b = b, a + b  # Обновляем значения a и b

# Пример использования
n = 10  # Количество чисел Фибоначчи, которые нужно сгенерировать
fib_sequence = list(fibonacci(n))  # Преобразуем генератор в список
print(fib_sequence)

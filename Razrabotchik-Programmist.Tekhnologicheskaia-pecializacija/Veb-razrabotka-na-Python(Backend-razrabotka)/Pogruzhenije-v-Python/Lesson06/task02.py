"""
Напишите модуль с функцией, которая принимает строку и возвращает строку с
удаленными дублирующимися подряд идущими символами. Например, строка
"aabbccaa" должна быть преобразована в "abca".
"""

def remove_consecutive_duplicates(s):
    """
    Функция принимает строку и возвращает строку с удаленными дублирующимися
    подряд идущими символами.

    :param s: Исходная строка
    :return: Строка с удаленными дублирующимися символами
    """
    if not s:  # Проверяем, является ли строка пустой
        return s

    result = [s[0]]  # Начинаем с первого символа
    for char in s[1:]:  # Проходим по всем символам, начиная со второго
        if char != result[-1]:  # Если текущий символ не равен последнему добавленному
            result.append(char)  # Добавляем его в результат

    return ''.join(result)  # Объединяем список символов в строку

# Пример использования
if __name__ == "__main__":
    example_string = "aabbccaa"
    result = remove_consecutive_duplicates(example_string)
    print(result)  # Вывод: "abca"

"""
Напишите программу, которая принимает строку и определяет, является ли она
палиндромом (читается одинаково с обеих сторон).
"""

def is_palindrome(input_str):
    # Убираем пробелы и приводим строку к нижнему регистру
    cleaned_str = ''.join(input_str.split()).lower()
    # Сравниваем строку с её обратной версией
    return cleaned_str == cleaned_str[::-1]

# Пример использования
input_string = input("Введите строку: ")
if is_palindrome(input_string):
    print("Строка является палиндромом.")
else:
    print("Строка не является палиндромом.")
